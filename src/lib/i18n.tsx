interface I18nContextType {
  locale: Locale;
  set_locale: (l: Locale) => void;
  t: (key: string, ...args: string[]) => string;
}

const I18nContext = createContext<I18nContextType>({
  locale: 'it',
  set_locale: () => {},
  t: (key) => key,
});

export const I18nProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const [locale, set_locale] = useState<Locale>('it');

  const t = useCallback((key: string, ...args: string[]) => {
    let text = translations[locale]?.[key] || translations['it']?.[key] || key;
    args.forEach((arg, i) => {
      text = text.replace(`{${i}}`, arg);
    });
    return text;
  }, [locale]);

  return (
    <I18nContext.Provider value={{ locale, set_locale, t }}>
      {children}
    </I18nContext.Provider>
  );
};

export const useI18n = () => useContext(I18nContext);

